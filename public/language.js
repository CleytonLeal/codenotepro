// Função para atualizar o conteúdo de acordo com o idioma selecionado
function updateLanguage(lang) {
    document.title = translations[lang].title;
    document.querySelector("meta[name='description']").setAttribute("content", translations[lang].metaDescription);
    document.querySelector("meta[name='keywords']").setAttribute("content", translations[lang].metaKeywords);

    document.getElementById("codeEditorTitle").innerText = translations[lang].nav.codeEditor;
    document.getElementById("menuHome").innerText = translations[lang].nav.home;
    document.getElementById("menuAbout").innerText = translations[lang].nav.about;
    document.getElementById("menuContact").innerText = translations[lang].nav.contact;
    document.getElementById("menuEditor").innerText = translations[lang].nav.codeEditor;
    document.getElementById("loginButton").innerText = translations[lang].nav.login;
    document.getElementById("loginButtonMobile").innerText = translations[lang].nav.loginMobile;
    document.getElementById("saveCodeButton").innerText = translations[lang].nav.save;
    document.getElementById("loadSavedCode").innerText = translations[lang].nav.load; // Adiciona a tradução para o botão "Carregar"

    document.getElementById("editorTitle").innerText = translations[lang].editor.editorTitle;
    document.querySelector("label[for='html']").innerText = translations[lang].editor.htmlLabel;
    document.querySelector("label[for='css']").innerText = translations[lang].editor.cssLabel;
    document.querySelector("label[for='js']").innerText = translations[lang].editor.jsLabel;
    document.getElementById("clear-html").innerText = translations[lang].editor.clearHTML;
    document.getElementById("clear-css").innerText = translations[lang].editor.clearCSS;
    document.getElementById("clear-js").innerText = translations[lang].editor.clearJS;
    document.getElementById("previewButton").innerText = translations[lang].editor.preview;
    document.getElementById("fullScreenButton").innerText = translations[lang].editor.fullScreen;
    document.getElementById("downloadButton").innerText = translations[lang].editor.download;
    document.getElementById("loadSavedCode").innerText = translations[lang].editor.loadCodeButton; // Atualização do texto do botão "Carregar Código"

    document.getElementById("loginTab").innerText = translations[lang].modal.loginTab;
    document.getElementById("registerTab").innerText = translations[lang].modal.registerTab;
    document.getElementById("email").setAttribute("placeholder", translations[lang].modal.email);
    document.getElementById("password").setAttribute("placeholder", translations[lang].modal.password);
    document.getElementById("user").setAttribute("placeholder", translations[lang].modal.username);
    document.getElementById("registerPassword").setAttribute("placeholder", translations[lang].modal.password);
    document.getElementById("confirmPassword").setAttribute("placeholder", translations[lang].modal.confirmPassword);
    document.getElementById("loginSubmit").innerText = translations[lang].modal.loginButton;
    document.getElementById("registerButton").innerText = translations[lang].modal.registerButton;
    document.getElementById("closeModal").innerText = translations[lang].modal.closeButton;
}
// Evento para o seletor de idioma
document.getElementById("languageSelector").addEventListener("change", function () {
    const selectedLang = this.value;
    updateLanguage(selectedLang);
});

